public class Player {
    private int rPos;
    private int cPos;

    public Player() {
        rPos = 0;
        cPos = 0;
    }

    public int getRPos() {
        return rPos;
    }

    public int getCPos() {
        return cPos;
    }

    public void setRPos(int r) {
        rPos = r;
    }

    public void setCPos(int c) {
        cPos = c;
    }
}
